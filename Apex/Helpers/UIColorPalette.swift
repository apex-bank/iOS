import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var DarkGreyBlue: UIColor {
        return UIColor(red: 46.0 / 255.0, green: 59.0 / 255.0, blue: 78.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkNavyBlue22: UIColor {
        return UIColor(red: 0.0, green: 0.0, blue: 25.0 / 255.0, alpha: 0.22)
    }
    
    @nonobjc class var deepSkyBlue: UIColor {
        return UIColor(red: 0.0, green: 122.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var deepSkyBlue25: UIColor {
        return UIColor(red: 0.0, green: 122.0 / 255.0, blue: 1.0, alpha: 0.25)
    }
    
    @nonobjc class var darkSkyBlue: UIColor {
        return UIColor(red: 74.0 / 255.0, green: 144.0 / 255.0, blue: 226.0 / 255.0, alpha: 1.0)
    }
    
    
    
}
